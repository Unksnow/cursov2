package com.cursos.repository;

import java.util.ArrayList;
import java.util.List;

import org.springframework.stereotype.Repository;

import com.cursos.model.Curso;

@Repository

public class CursoRepository {

    private List<Curso> listarCursos = new ArrayList<>();


    public List<Curso> obtenerCursos(){
        return listarCursos;
    }

    public Curso buscarCursoPorId(int idcurso){
        for (Curso curso : listarCursos) {
            if (curso.getIdcurso()== idcurso){
                return curso;
            }
        }
        return null;
    }

    public Curso guardarCurso(Curso cur){
        listarCursos.add(cur);
        return cur;
    }

    public void eliminar(int idcurso){
        int idPosition = 0;
        for(int i = 0; i< listarCursos.size(); i++){
            if(listarCursos.get(i).getIdcurso()==idcurso){
                idPosition = 1;
                break;
            }
        }
        if(idPosition >=0){
            listarCursos.remove(idPosition);
        }
        return;
   
    }





}
